package com.smhrd;

public class ShotVO {

	
	private int shot_seq;
	private int vac_seq;
	private int hos_seq;
	private String shot_age;
	private int shot_times;
	private String shot_gender;
	private int shot_price;
	
	public int getShot_seq() {
		return shot_seq;
	}
	public void setShot_seq(int shot_seq) {
		this.shot_seq = shot_seq;
	}
	public int getVac_seq() {
		return vac_seq;
	}
	public void setVac_seq(int vac_seq) {
		this.vac_seq = vac_seq;
	}
	public int getHos_seq() {
		return hos_seq;
	}
	public void setHos_seq(int hos_seq) {
		this.hos_seq = hos_seq;
	}
	public String getShot_age() {
		return shot_age;
	}
	public void setShot_age(String shot_age) {
		this.shot_age = shot_age;
	}
	public int getShot_times() {
		return shot_times;
	}
	public void setShot_times(int shot_times) {
		this.shot_times = shot_times;
	}
	public String getShot_gender() {
		return shot_gender;
	}
	public void setShot_gender(String shot_gender) {
		this.shot_gender = shot_gender;
	}
	public int getShot_price() {
		return shot_price;
	}
	public void setShot_price(int shot_price) {
		this.shot_price = shot_price;
	}
	
	
}
